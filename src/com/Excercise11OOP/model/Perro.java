package com.Excercise11OOP.model;

public class Perro 
{
	public int id;
	private String name;
	protected String breed;
	public int age;
	public double Height;
	
	public Perro()
	{
		
	}
	
	public Perro(int id, String name, String breed, int age, double Height)
	{
		this.id=id;
		this.name=name;
		this.breed=breed;
		this.age=age;
		this.Height=Height;
	}

	
		
	
	

	
	
	
	
}
